/**
 * Một từ hoặc một câu được gọi là pangram nếu tất cả các ký tự trong bảng chữ cái xuất hiện ít nhất một lần. Pangram thường được sử dụng
 * để kiểm chứng phông chữ trong in ấn hoặc kiểm tra các thiết bị in ấn. Bạn được cung cấp một chuỗi bao gồm các chữ cái Latinh viết
 * thường và viết hoa. Kiểm tra xem chuỗi này có phải là một pangram hay không. Chúng ta xem rằng chuỗi chứa một ký tự trong bảng chữ
 * cái Latinh nếu ký tự này xuất hiện trong chuỗi ở dạng chữ hoa hoặc chữ thường.
 *
 * Input:
 * Dòng đầu tiên chứa một số nguyên dương n (1≤n≤100) - số ký tự của chuỗi. Dòng thứ hai chứa chuỗi. Chuỗi chỉ bao gồm các chữ cái
 * Latinh viết hoa và viết thường.
 *
 * Output:
 * Xuất "YES", nếu xâu là một pangram và ngược lại xuất "NO".
 *
 * Example 1:
 * Input:
 * 12
 * toosmallword
 *
 * Output:
 * NO
 * 
 * Example 2:
 * Input:
 * 35
 * TheQuickBrownFoxJumpsOverTheLazyDog
 *
 * Output:
 * YES
 **/
